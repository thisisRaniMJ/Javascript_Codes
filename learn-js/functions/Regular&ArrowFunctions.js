//regular function without parameters
function hello()
{
    console.log("hello from regular function")
}
hello()

//arrow funtion without parameters
h = () =>
{
    console.log("hello from arrow function")
}
h()

//regular function with parameters
function add(a,b,c)
{
    console.log(a+b+c)
}
add(10,20,15)

//arrow funtion with= parameters
sum = (a,b,c) =>
{
    console.log(a+b+c)
}
sum(100,200,300)
//operators in js

a = 10
b = 5
c = "5"

//arithmetic operators
console.log(a + b)
console.log(a - b)
console.log(a * b)
console.log(a / b)
console.log(a % b)

//comparison operators
console.log(a < b)
console.log(a > b)
console.log(a <= b)
console.log(a >= b)
console.log(b == c) //is 5 = 5?
console.log(b === c) // is 5 = 5? and is number = string?

//logical && operators
console.log(true && true)
console.log(true && false)
console.log(false && true)
console.log(false && false)

//logical || operators
console.log(true || true)
console.log(true || false)
console.log(false || true)
console.log(false || false)

//logical not operator
console.log(!true)
console.log(!false)

//ternary operator
console.log(a > b ? 10:5)